package C_BitMagic;

public class BitwiseOperators {
    public static void main(String[] args) {
        int x=3;
        int y=6;
        
        //Bitwise AND - &
        System.out.println(x&y);

        //Bitwise OR - |
        System.out.println(x|y);
        
        //Bitwise XOR - ^
        System.out.println(x^y);
    }
}
